public class MaxDepth {
    //递归返回每个叶子的深度
    public int maxDepth(TreeNode root) {
        return depth(root,1);
    }

    //递归函数
    public int depth(TreeNode node, int n){
        if (node == null)
            return n;
        n++;
        return Math.max(depth(node.right,n),depth(node.left,n));
    }
}
