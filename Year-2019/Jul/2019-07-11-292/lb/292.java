public class CanWinNim {
    //Nim游戏
    //动态方程 dp[n] = !dp[n-3] || !dp[n-2] || !dp[n-1]
    //dp[1,2,3] = true -- 超出空间限制
    //4的倍数都为false
    public boolean canWinNim(int n) {
        //3 - true
        //4 - false
        //5 - true
        //6 - 4 - true
        //7 - 4 - true
        //8 - 5，6，7 - false
        //9 - true
        return !(n%4 == 0);
    }
}
