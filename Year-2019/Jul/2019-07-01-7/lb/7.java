public class Reverse {
    //判断正负
    //溢出判断--通过异常判断
    //使用stringBuffer的reverse
    public int reverse(int x) {
        boolean plus = true;
        if (x < 0){
            x = -x;
            plus = false;
        }
        StringBuffer stringBuffer = new StringBuffer(String.valueOf(x));
        stringBuffer.reverse();
        if (!plus){
            stringBuffer = new StringBuffer("-"+stringBuffer);
        }
        try {
            int result = Integer.parseInt(stringBuffer.toString());
            return result;
        }catch (NumberFormatException e){
            return 0;
        }
    }
}
