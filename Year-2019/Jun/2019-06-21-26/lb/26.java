    //删除排序数组中的重复项
    //双指针循环
    public int removeDuplicates(int[] nums) {
        int result = 0;
        int left = 0;
        int lastNum = Integer.MIN_VALUE;
        for (int i = 0; i < nums.length; i++) {
            if (lastNum != nums[i]){
                result++;
                lastNum = nums[i];
                nums[left] = nums[i];
                left++;
            }
        }
        return result;
    }
