/**
 * @program: idea
 * @description: 最大子序和
 * @author: Lovely Dragon
 * @create: 2020-05-09 08:49
 */
public class MaxSubArray {
    public int maxSubArray(int[] nums) {
        if (nums.length == 0)
            return 0;
        int max = nums[0];
        int temp = nums[0];
        for (int i = 1; i < nums.length; i++) {
            if (temp < 0)
                temp = nums[i];
            else
                temp += nums[i];
            max = max > temp ? max : temp;
        }
        return max;
    }
}
